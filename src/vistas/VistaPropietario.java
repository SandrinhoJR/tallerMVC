    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        String documento = txtDoc.getText();
        String nombre = txtNombre.getText();
        String telefono = txtTel.getText();
        Propietario propietario = new Propietario(documento, nombre, telefono);
        boolean respuesta = controlador.editar(propietario);
        if (respuesta) {
            JOptionPane.showMessageDialog(null, "El propietario se edit√≥ correctamente");
            llenarTabla();
            LimpiarCampos();
        } else {
            JOptionPane.showMessageDialog(null, "El propietario lastimosamente no se puedo editar ");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String documento = txtDoc.getText();
        Propietario propietario = controlador.buscar(documento);
        if (propietario != null) {
            txtNombre.setText(propietario.getNombre());
            txtTel.setText(propietario.getTelefono());
        } else {
            JOptionPane.showMessageDialog(null, "El propietario NO se encontr√≥, revisa que los datos sean correctos");
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String documento = txtDoc.getText();
        String nombre = txtNombre.getText();
        String telefono = txtTel.getText();
        Propietario propietario = new Propietario(documento, nombre, telefono);
        boolean respuesta = controlador.guardar(propietario);
        if (respuesta) {
            JOptionPane.showMessageDialog(null, "El Propietario se guard√≥");
            llenarTabla();
            LimpiarCampos();
        } else {
            JOptionPane.showMessageDialog(null, "El Propietario No se pudo guardar");
        }

    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String documento = txtDoc.getText();
        boolean respuesta =  controlador.eliminar(documento);
        if (respuesta) {
            JOptionPane.showMessageDialog(null, "Se elimin√≥ correctamente el propietario");
            llenarTabla();
            LimpiarCampos();
        } else {
            JOptionPane.showMessageDialog(null, "el propietario no se elimin√≥");
        }
    }

    private void LimpiarCampos() {
        txtNombre.setText("");
        txtDoc.setText("");
        txtTel.setText("");

    }//GEN-LAST:event_btnEliminarActionPerformed

